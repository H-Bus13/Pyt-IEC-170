import math

#pi = 3.14
pi = math.pi

radio = float(input("Ingrese el radio de la circunferencia: "))
print()

area = pi * radio**2
print(f"El area de la circunferencia de radio {radio} = {area}") 

numero = float(input("Ingrese un número: "))
print()
raiz_cuadrada = math.sqrt(numero)
print(f"La raíz cuadrada de {numero} = {raiz_cuadrada}") 
